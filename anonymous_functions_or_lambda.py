import math
f=lambda a:a*a  #these are the functions which are not defined by any name and are defined at the time we need that function

result=f(5)
print(result)

root=lambda a:math.sqrt(a)
sqrt=root(20)
print(sqrt)

add=lambda a,b:a+b  #lambda functions:  it should be of one expression only
addition=add(20,30)
print(addition)

